export function getProductFaqsByCategory(
  categoryName?: string,
  categorySlug?: string,
): readonly SeoFaqItem[] {
  if (!categoryName && !categorySlug) return PRODUCTS_FAQS;

  const roomKey = detectRoomKey(categoryName) ?? detectRoomKey(categorySlug);
  if (roomKey) {
    return getRoomSpecificFaqs(roomKey);
  }

  if (!categoryName) return PRODUCTS_FAQS;

  if (categoryName.includes("Tek Kat")) {
    return SINGLE_STOREY_PRODUCT_FAQS;
  }

  if (categoryName.includes("Çift Kat") || categoryName.includes("Dubleks")) {
    return DOUBLE_STOREY_PRODUCT_FAQS;
  }

  if (categoryName.includes("Çelik")) {
    return STEEL_HOUSE_PRODUCT_FAQS;
  }

  return PRODUCTS_FAQS;
}
